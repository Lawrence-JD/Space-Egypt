#include "ISavior2Editor.h"
#include "Savior2Editor.h"
#include "Savior2Style.h"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define LOCTEXT_NAMESPACE "Synaptech"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class FSavior2Editor : public ISavior2Editor {
public:
	virtual void StartupModule() override {
		IAssetTools& AssetTools = FModuleManager::LoadModuleChecked<FAssetToolsModule>("AssetTools").Get();
		SY_AssetCategory = AssetTools.RegisterAdvancedAssetCategory(FName(TEXT("Synaptech")),LOCTEXT("SynaptechCategory","Synaptech"));
		//
		{
			TSharedRef<IAssetTypeActions> ATA_Savior2 = MakeShareable(new FATA_Savior2);
			AssetTools.RegisterAssetTypeActions(ATA_Savior2);
		}
		//
		FSavior2Style::Initialize();
	}
};

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#undef LOCTEXT_NAMESPACE

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

IMPLEMENT_MODULE(FSavior2Editor,Savior2Editor);

